    private void jButtonOperationalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOperationalActionPerformed
        this.myFrameToAgentComm.launchNewOperational(this.jTextFieldOperational_ID.getText(), this.jTextFieldOperationalName.getText(), this.jTextFieldOperationalHardwareType.getText(), this.opLocationsChoosen, this.OperationalPriorityTextField.getText());
        this.jButtonSupervisor.setEnabled(true);
        this.jTextFieldSupervisorId.setEnabled(true);
        this.jTextFieldSupervisorName.setEnabled(true);
        this.jTextFieldSupervisorOperationals.setEnabled(true);

        if (this.firstTimeOP) {
            this.OPs = this.jTextFieldOperationalName.getText() + ";";
            this.firstTimeOP = false;
        } else {
            this.OPs = this.OPs + this.jTextFieldOperationalName.getText() + ";";
        }

        this.jTextFieldSupervisorOperationals.setText(this.OPs);

        if (this.task2Combo.get(this.jTextFieldOperationalHardwareType.getText()) == null) {
            this.jComboBoxTaskHardware.addItem(this.jTextFieldOperationalHardwareType.getText());
            this.task2Combo.put(this.jTextFieldOperationalHardwareType.getText(), true);
        }
    }//GEN-LAST:event_jButtonOperationalActionPerformed

