    public void finishProduct(Item myItem) {
        int timeToProduce = (int) ((System.currentTimeMillis() - myItem.getEntryDate()) / 1000);
        produced.add(timeToProduce);
        int mean = 0;
        for (Integer next : produced) {
            mean += next;
        }
        mean = mean / produced.size();
        this.jTextFieldMeanTime.setText("" + mean);
        this.jTextAreaExits.append(myItem.getID() + "\t Time to Produce (seconds): " + timeToProduce);
        this.jTextAreaExits.append("\n");
    }

